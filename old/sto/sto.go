var Vars map[string]any

// *****************************************************************************
// init()
// *****************************************************************************
func init() {
	Vars = make(map[string]any)
}

// *****************************************************************************
// Sto()
// *****************************************************************************
func Sto(n string, v any) {
	Vars[n] = v
}

// *****************************************************************************
// Rcl()
// *****************************************************************************
func Rcl(n string) any {
	return Vars[n]
}
